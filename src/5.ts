// Homework Problem 1: Find the area of a circle
const calculateCircleArea = (radius: number): number => {
  return Math.PI * radius ** 2;
};

// Homework Problem 2: Find the volume of a cube
const calculateCubeVolume = (sideLength: number): number => {
  return sideLength ** 3;
};

// Homework Problem 3: Find the perimeter of a rectangle
const calculateRectanglePerimeter = (width: number, height: number): number => {
  return 2 * (width + height);
};
